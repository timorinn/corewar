#include "libft.h"

int		ft_atoi(const char *s)
{
	unsigned long long int	n;
	int						sign;

	sign = 1;
	n = 0;
	while ((*s >= 9 && *s <= 13) || *s == ' ')
		s++;
	if (*s == '+' || *s == '-')
		sign *= (*s++ == '-' ? -1 : 1);
	while (ft_isdigit(*s))
		n = n * 10 + (*s++) - 48;
	if (n > 4294967295)
		return (sign == 1 ? -1 : 0);
	return (sign * n);
}
