#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int i;

	if (f != NULL && s != NULL)
	{
		i = 0;
		while (*s != '\0')
		{
			f(i, s);
			s++;
			i++;
		}
	}
}
