#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int z;

	i = 0;
	z = 0;
	while (s1[i])
		i++;
	while (s2[z])
	{
		s1[i + z] = s2[z];
		z++;
	}
	s1[i + z] = '\0';
	return (s1);
}
